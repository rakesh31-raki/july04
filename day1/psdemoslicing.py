"""
syntax for the slicing
~~~~~~~~~~~~~~~~~~~~~~

    name[start-index:ex-of-end-index]

syntax for the extended slicing
    name[start-index:ex-of-end-index:step]

"""

s = 'perlandpython'
print(s[:4])
print(s[3:7])
print(s[-6:])
print(s[:])
print(s[1:-1])
print(s[-6:-4])
print(s[::-1])
print(s[::-1] == s)
print()

